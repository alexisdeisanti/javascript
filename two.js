
// Make a variable called "temperature." 
var temp = 90;

// Write some code that will alert the user various messages
// depending on the weather.

// If it is less than 0 degrees, stay inside.
if(temp < 0){
	alert("stay inside");
}
// If it is less than 30 degrees, wear a coat and a hat.
else if (0 < temp && temp < 30){
	alert("wear a coat and a hat")
}

// If it is less than 50 degrees, wear a coat.
else if (30 < temp && temp < 50){
	alert("wear a coat")}

// Otherwise, wear whatever you want.
else {
	alert("wear whatever you want")
}




